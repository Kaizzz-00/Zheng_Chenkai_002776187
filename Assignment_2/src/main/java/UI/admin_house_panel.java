    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        // TODO add your handling code here:
        int slctRowIndex= jTable1.getSelectedRow();
        if (slctRowIndex<0)  {
            JOptionPane.showMessageDialog(this, "Please Select A Row To Delete.");
            return;
        }// if nothing was selected, then inform the user

        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        House selectedProfile=(House) model.getValueAt(slctRowIndex,0);

        houseDirectory.deleteHouse(selectedProfile);// to delete the selected profile, need to create a void
        JOptionPane.showMessageDialog(this, "Selected Profile Deleted.");
        displayTable();//refresh table
    }//GEN-LAST:event_delBtnActionPerformed

    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        int slctRowIndex= jTable1.getSelectedRow();
        if (slctRowIndex<0)  {
            JOptionPane.showMessageDialog(this, "Please Select A Row To Edit.");
            return;
        }// if nothing was selected, then inform the user

        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        House sh=(House) model.getValueAt(slctRowIndex,0);

        txtAddress.setText(sh.getAddress());
        txtApt.setText(sh.getAptnum());
        txtBlock.setText(sh.getName());
        txtCity.setText(sh.getCom().getCity().getCityName());
        txtThecom.setText(sh.getCom().getComName());
    }//GEN-LAST:event_viewBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int slctRowIndex= jTable1.getSelectedRow();
        if (slctRowIndex<0)  {
            JOptionPane.showMessageDialog(this, "Please Select A Row To Edit.");
            return;
        }// if nothing was selected, then inform the user

        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        House sh=(House) model.getValueAt(slctRowIndex,0);
        sh.setAddress(txtAddress.getText());
        sh.setAptnum(txtApt.getText());
        sh.setName(txtBlock.getText());
        JOptionPane.showMessageDialog(this, "House Info Updated.");
        txtAddress.setText("");
        txtApt.setText("");
        txtBlock.setText("");
        txtCity.setText("");
        txtThecom.setText("");
        displayTable();
    }//GEN-LAST:event_editBtnActionPerformed

    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        String hname=txtAddress.getText();
        String hapt=txtApt.getText();
        String hblock=txtBlock.getText();
        
        
        if (!(hapt.matches("[0-9]+") && hapt.length() >2 )) {
            JOptionPane.showMessageDialog(this, "Valid AptNumber Required");
            txtApt.setText(""); // validate the input email
            return;
        }
        House newHouse=houseDirectory.addnewHouse();
        
        Object slctCom=comBox.getSelectedItem();
        for (Community com:comDirectory.getHistory())
        {
            if (com.getComName().equals(slctCom.toString())){
            newHouse.setCom(com);
            }
        }

        
        newHouse.setAddress(hname);
        newHouse.setAptnum(hapt);
        newHouse.setName(hblock);
        JOptionPane.showMessageDialog(this, "New House Added");
        txtAddress.setText("");
        txtApt.setText("");
        txtBlock.setText("");
        txtCity.setText("");
        txtThecom.setText("");
        displayTable();
    }//GEN-LAST:event_createBtnActionPerformed

