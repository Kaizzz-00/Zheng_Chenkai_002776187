     private void displayTable() {
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        //create a model to display the profile history
        
        
        for (House hu:houseDirectory.getDirectory()){
            Object[] row = new Object[11];
            row[0]=hu;
            //row[1]= pa.getName();
            row[1]= hu.getAptnum();
            row[2]= hu.getName();
            if(hu.getCom()!=null){
                row[3]=hu.getCom().getCity().getCityName();
                row[4]=hu.getCom().getComName();
            }
            // use a small array to display each attributes
            model.addRow(row);

            
            
            
        }
    }
    private void displayCom(){
    for (Community com:comDirectory.getHistory())
        {
            comBox.addItem(com.getComName());
        }
    }

